    private void view_Table(){
        String query = "SELECT * FROM borrow";
    
        try {
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();
        
            DefaultTableModel model = (DefaultTableModel) borrow_table.getModel();
            model.setRowCount(0);
        
            while (rs.next()) {
                String borrow = rs.getString("borrow_id");
                String std_name = rs.getString("Student_name");
                String book = rs.getString("book_name");
                String date = rs.getString("date_borrowed");
                String r_date = rs.getString("date_return");

                model.addRow(new Object[]{borrow, std_name, book, date, r_date}); 
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

