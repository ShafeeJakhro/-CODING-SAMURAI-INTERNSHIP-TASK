    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String b = borrow_id.getText();
        
        String query = "DELETE FROM borrow WHERE borrow_id=?";
        try (PreparedStatement preparedStatement = con.prepareStatement(query)) {
            preparedStatement.setString(1, b);
        
            int rowsAffected = preparedStatement.executeUpdate();
            if (rowsAffected > 0) {
                borrow_count--;
                
                String q = "INSERT INTO borrow (borrow_no) VALUES (?)";
                try (PreparedStatement preparedStatement2 = con.prepareStatement(q)) {
                    preparedStatement2.setInt(1, borrow_count); 
                    preparedStatement2.executeUpdate();
                }
                JOptionPane.showMessageDialog(null, "Data deleted successfully.");
                view_Table();
                
                borrow_id.setText("");
                borrow_name.setText("");
                borrow_book.setText("");
                borrow_date.setDate(null);
                return_date.setDate(null);
            } else {
                JOptionPane.showMessageDialog(null, "No record found with the provided student ID.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void borrow_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_borrow_tableMouseClicked
        DefaultTableModel tmodel = (DefaultTableModel)borrow_table.getModel();
        int selectedIndex = borrow_table.getSelectedRow();
        
        borrow_id.setText(tmodel.getValueAt(selectedIndex, 0).toString());
        borrow_name.setText(tmodel.getValueAt(selectedIndex, 1).toString());
        borrow_book.setText(tmodel.getValueAt(selectedIndex, 2).toString());
        try {
            java.util.Date borrowDate = new SimpleDateFormat("yyyy-MM-dd").parse(tmodel.getValueAt(selectedIndex, 3).toString());
            java.util.Date returnDate = new SimpleDateFormat("yyyy-MM-dd").parse(tmodel.getValueAt(selectedIndex, 4).toString());
            borrow_date.setDate(borrowDate);
            return_date.setDate(returnDate);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Error parsing date: " + e.getMessage());
        }
    }//GEN-LAST:event_borrow_tableMouseClicked

